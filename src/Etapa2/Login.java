    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        String email = txtEmail.getText();
        String senha = txtSenha.getText();
        boolean login = false;
        
        for(Usuario u : cdUser.getUsuarios()){
            if(email.equalsIgnoreCase(u.getEmail()) && senha.equals(u.getSenha())){
                login = true;
                break;
            }
        }
        
        if(login == true){
            JOptionPane.showMessageDialog(null, "Login efetuado com sucesso");
            Pesquisar search = new Pesquisar();
            search.setVisible(true);
            this.dispose();
        } else{
            JOptionPane.showMessageDialog(null, "E-mail ou senha est√£o incorretos. Por favor, verifique e tente novamente.");
        }
    }//GEN-LAST:event_btnEntrarActionPerformed
